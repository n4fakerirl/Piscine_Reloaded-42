#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	x;

	x = 0;
	if (nb < 0)
		return (0);
	while ((x * x) != nb && x < nb)
	{
		x++;
	}
	if (x >= nb)
		return (0);
	return (x);
}
